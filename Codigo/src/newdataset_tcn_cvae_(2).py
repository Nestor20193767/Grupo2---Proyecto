"""## 11. Arquitectura TCN-cVAE (Dimensiones Dinámicas)"""

# ─────────────────────────────────────────────────────────
#  HIPERPARÁMETROS – Detectados dinámicamente desde ECG5000
#  INPUT_LENGTH y N_CLASSES se obtienen del dataset real,
#  no de constantes fijas de otro dataset.
# ─────────────────────────────────────────────────────────

