NUM_CLASSES  = N_CLASSES              # Número real de clases

LATENT_DIM   = 32
HIDDEN_CH    = 64
DILATIONS    = (1, 2, 4, 8)
KERNEL_SIZE  = 5
DROPOUT_RATE = 0.1

